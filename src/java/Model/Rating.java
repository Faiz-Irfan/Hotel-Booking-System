package Model;

/**
 *
 * @author FAIZ
 */
public class Rating {
    
    String email;
    String rating;
    String review; 
    String roomType;

    public Rating(String email, String rating, String review, String roomType) {
        this.email = email;
        this.rating = rating;
        this.review = review;
        this.roomType = roomType;
    }
    

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getRating() {
        return rating;
    }

    public void setRating(String rating) {
        this.rating = rating;
    }

    public String getReview() {
        return review;
    }

    public void setReview(String review) {
        this.review = review;
    }

    public String getRoomType() {
        return roomType;
    }

    public void setRoomType(String roomType) {
        this.roomType = roomType;
    }
    
    
}
